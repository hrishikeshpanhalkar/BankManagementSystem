    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String name=txtname.getText();
        String password=String.valueOf(txtpass.getPassword());
        
        if(name.isEmpty() || password.isEmpty() || txtacc.getText().isEmpty() || txtbalance.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Please enter value");
        }else{ 
            int accno=Integer.parseInt(txtacc.getText());
            int balance=Integer.parseInt(txtbalance.getText());
            int i=BankDoa.withdraw(accno,name,password,balance);
            if(i>0){
                JOptionPane.showMessageDialog(this,"Your balance is decrease to "+balance);
                dispose();
                BankSuccess.main(new String[]{});
            }else{
                JOptionPane.showMessageDialog(this,"Unble to withdraw balance");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

